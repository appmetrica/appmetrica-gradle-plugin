pluginManagement {
    val agpVersion = providers.gradleProperty("agpVersion").get()
    repositories {
        mavenLocal()
        google()
        mavenCentral()
        gradlePluginPortal()
    }
    resolutionStrategy {
        eachPlugin {
            if (requested.id.id == "com.android.application") {
                useModule("com.android.tools.build:gradle:$agpVersion")
            }
        }
    }
}

